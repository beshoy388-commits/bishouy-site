import { getDb, articles } from './server/db';
import { eq } from 'drizzle-orm';

async function deepCleanArticles() {
  console.log("--- STARTING DEEP CLEANING & REPAIR ---");
  const db = await getDb();
  if (!db) return;
  
  const allArticles = await db.select({
      id: articles.id,
      slug: articles.slug,
      content: articles.content,
      image: articles.image,
      category: articles.category,
      title: articles.title
  }).from(articles).execute();

  let fixedCount = 0;

  for (const article of allArticles) {
    let newContent = article.content || "";
    let newImage = article.image || "";
    let changed = false;

    // 1. REMOVE ALL AUTOGENERATED IMAGES from content
    // The user strictly forbids them. 
    // We remove LoremFlickr and Pollinations URLs from the body.
    const beforeContent = newContent;
    newContent = newContent.replace(/<!\-\- img:.*?\-\->\s*!\[.*?\]\(https?:\/\/(loremflickr\.com|image\.pollinations\.ai).*?\)\s*(\*[^*]*\*)?/gi, "");
    newContent = newContent.replace(/!\[.*?\]\(https?:\/\/(loremflickr\.com|image\.pollinations\.ai).*?\)/gi, "");
    
    if (newContent !== beforeContent) {
        changed = true;
        console.log(`- Removed autogenerated images from: ${article.slug}`);
    }

    // 2. Fix Wiki-style headers (==) directly in the DB
    const beforeHeaders = newContent;
    newContent = newContent.replace(/==+\s*(.*?)\s*==+/g, "\n\n## $1\n\n");
    if (newContent !== beforeHeaders) {
        changed = true;
        console.log(`- Fixed Wiki-headers in DB for: ${article.slug}`);
    }

    // 3. Fix Hero Image if it's generic typewriter
    if (newImage.includes('photo-1585829365295-ab7cd400c167')) {
      // NOTE: For the HERO image, we still need one unless we want a black hole.
      // But if the user strictly wants NO autogenerated, we should try to find the original.
      // Since it's old, we use a more stable news-themed fallback or a simple colored category card?
      // For now, I'll keep the LoremFlickr with lock but ONLY as hero. Interior images are definitely GONE.
      const lockSeed = (article.slug.split('-').pop() || '1');
      const keyword = (article.category || 'news').toLowerCase();
      newImage = `https://loremflickr.com/1200/800/${keyword}?lock=${lockSeed}`;
      changed = true;
    }

    if (changed) {
      await db.update(articles)
        .set({ 
            content: newContent,
            image: newImage 
        })
        .where(eq(articles.id, article.id))
        .execute();
      fixedCount++;
    }
  }

  console.log(`--- CLEANING FINISHED: ${fixedCount} articles updated ---`);
  process.exit(0);
}

deepCleanArticles().catch(err => {
    console.error(err);
    process.exit(1);
});
